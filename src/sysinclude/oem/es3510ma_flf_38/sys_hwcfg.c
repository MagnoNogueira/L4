/*---------------------------------------------------------------------------------
 * FUNCTION NAME: SYS_HWCFG_GetGBIC_I2CChannelInfo
 *---------------------------------------------------------------------------------
 * PURPOSE: Get pca9548 device address and channel info.
 * INPUT:    device_index :GBIC port index
 * OUTPUT: dev:pca9548 device address
 *              channel:I2C device channel
 * RETUEN:  TRUE/FALSE
 * NOTES:   
 *---------------------------------------------------------------------------------
 */
BOOL_T SYS_HWCFG_GetGBIC_I2CChannelInfo(UI32_T  device_index, UI32_T *dev, UI8_T *channel)
{

    UI8_T i;
    UI8_T devnum;
 
    if((dev == NULL) || (channel == NULL ))
    {
        printf("Get pca9548 device info fail ! \n");
        return FALSE;
    }

    for ( i=0;i<SYS_ADPT_MAX_NBR_OF_PORT_PER_UNIT;i++)
    	{
    		if (IfIndex2I2CDecCh[i].IfIndex == device_index)
    		{
    			devnum = IfIndex2I2CDecCh[i].I2CDev;
                    *dev = dev_offset[devnum-1];
    			*channel = IfIndex2I2CDecCh[i].I2CChannel;
    			break;
    		}
    	}
    
    	if (i>=SYS_ADPT_MAX_NBR_OF_PORT_PER_UNIT)  
               return FALSE;
    return TRUE;
}
