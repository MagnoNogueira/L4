BOOL_T SYS_HWCFG_Getledproc(UI8_T *ledproc, int device_id)
{
    UC_MGR_Sys_Info_T         uc_sys_info;
   
    if (!UC_MGR_GetSysInfo(&uc_sys_info))
    {
        perror("\r\nGet UC System Information Fail.");
        
        /* severe problem, while loop here
         */
        while (TRUE);        
    }
   
    if ((uc_sys_info.board_id == 0)&& (device_id == 0))
    {   
        memcpy(ledproc, ledproc_ES4626H_24GE, LED_PROGRAM_RAM_SIZE);
    }    
    else /* 26 port or 50 port chip 1 */
    {
    	if ((uc_sys_info.board_id == 1))
    	{
    	    memcpy(ledproc, ledproc_ES4626H_24GE_XE25_XE26, LED_PROGRAM_RAM_SIZE);
    	}
    	else
    	{
    		memcpy(ledproc, ledproc_ES4626H_24GE_XE27_XE28, LED_PROGRAM_RAM_SIZE);
        }              	
    }

   return TRUE;
}

